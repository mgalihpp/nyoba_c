#include <iostream>

int main() {
    int number = 10;

    // Conditional Statement
    if (number > 0) {
        std::cout << "Number is positive." << std::endl;
    } else {
        std::cout << "Number is negative or zero." << std::endl;
    }

    // Loop Statement
    for (int i = 0; i < 5; i++) {
        std::cout << "i: " << i << std::endl;
    }

    return 0;
}
